//OPERADORES IGUAL A

const num1 = 20;
const num2 = "20";
const num3 = 30;

//Comparadores
console.log(num1 == num1);
console.log(num1 == num3);

//Comparador estricto
console.log(num1 === num2);
console.log(num1 === parseInt(num2));
console.log(typeof num1);
console.log(typeof num2);

//Diferente
const pass = "admin";
const pass2= "Admin";

console.log(pass != pass2);
console.log(num1 != num2);
console.log(num1 !== num2);

